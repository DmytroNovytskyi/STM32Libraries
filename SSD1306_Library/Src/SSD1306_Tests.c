void SSD1306_TestBorder(SSD1306_HandleTypeDef *handle) {
	SSD1306_Fill(handle, BLACK);

	uint8_t x = 0;
	uint8_t y = 0;
	do {
		SSD1306_DrawPixel(handle, x, y, BLACK);

		if ((y == 0) && (x < (handle->width - 1)))
			x++;
		else if ((x == (handle->width - 1)) && (y < (handle->height - 1)))
			y++;
		else if ((y == (handle->height - 1)) && (x > 0))
			x--;
		else
			y--;

		SSD1306_DrawPixel(handle, x, y, WHITE);
		SSD1306_UpdateScreen(handle);

		HAL_Delay(5);
	} while (x > 0 || y > 0);

	HAL_Delay(1000);
}

